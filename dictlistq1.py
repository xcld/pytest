'''
一、元素分类

有如下值集合 [11,22,33,44,55,66,77,88,99,90...]，将所有大于 66 的值保存至字典的第一个key中，将小于 66 的值保存至第二个key的值中。
即： {'k1': 大于66的所有值, 'k2': 小于66的所有值}
'''

new=[11,22,33,44,55,66,77,88,99,90]
new1={"k1":[],"k2":[]}
for a in new:
    if a > 66:
        new1["k1"].append(a)
    else:
        new1["k2"].append(a)
print(new1)